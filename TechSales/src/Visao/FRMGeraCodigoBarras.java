    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            //Utilizando o BarcodeFactory para a geração de codigos de barras
            //Utilizaremos o createCode128, por ser um padrão, mas caso queira um outro formato (tamanho e largura diferentes) poderá usar qualquer outro tipo!
            //Utilizando o JOptionPane para o usuário entrar com o código no qual será gerado um código de barras.
            Barcode barcode = BarcodeFactory.createCode128(JOptionPane.showInputDialog("Entre com o número do código de barras a ser gerado"));
            //Utilizando o PrinterJob para a impressão do código de barras
            //PrinterJob é nativo do java
            PrinterJob printerJob = PrinterJob.getPrinterJob();
            //Setando o que será imprimido
            printerJob.setPrintable(barcode);
            //Abrindo uma caixa de diálogo para a impressão do código..
            if (printerJob.printDialog()) {
                try {
                    printerJob.print();
                } catch (PrinterException ex) {
                    Logger.getLogger(FRMGeraCodigoBarras.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } catch (BarcodeException ex) {
            Logger.getLogger(FRMGeraCodigoBarras.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

