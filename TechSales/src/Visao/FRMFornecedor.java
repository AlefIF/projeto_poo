    private DefaultTableModel criaTabela() {
        //sempre que usar JTable é necessário ter um DefaulttableModel
        DefaultTableModel dTable = new DefaultTableModel() {
            //Define o tipo dos campos (coluna) na mesma ordem que as colunas foram criadas
            Class[] types = new Class[]{
                java.lang.Integer.class,
                java.lang.String.class,
                java.lang.String.class,
                java.lang.Double.class,
                java.lang.String.class,
                java.lang.String.class,
                java.lang.String.class,
                java.lang.String.class,};
            //define se os campos podem ser editados na propria tabela
            boolean[] canEdit = new boolean[]{
                false, false, false, false, false, false, false, false};

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        ;

        };
        //retorna o DefaultTableModel
    return dTable;
    }

    private void preencheTabela() {
        dTable = criaTabela();
        //seta o nome das colunas da tabela
        dTable.addColumn("Código");
        dTable.addColumn("Nome");
        dTable.addColumn("Telefone");
        dTable.addColumn("Email");
        dTable.addColumn("CNPJ/CPF");
        dTable.addColumn("Anotações");
        //pega os dados do ArrayList
        fDados = forCon.listarALL();

        for (FornecedorBEAN d : fDados) {
            dTable.addRow(new Object[]{d.getForCodigo(), d.getForNomeEmpresa(),
                d.getForTelefoneContato(), d.getForEmail(), d.getForCNPJ(),
                d.getForAnotacoes()});
        }

        tableFornecedor.setModel(dTable);
    }

    public boolean verificaCampos() {
        if (tfNome.getText().equals("")
                || cbTipo.getSelectedIndex() == 0
                || tfEmail.getText().equals("") || tfTelefone.getText().equals("")
                || tfanotacao.getText().equals("")
                || tfRua.getText().equals("")
                || tfNumero.getText().equals("")
                || tfBairro.getText().equals("")
                || tfCidade.getText().equals("")
                || tfEstado.getText().equals("")
                || tfPais.getText().equals("")) {

            return false;
        } else {
            return true;
        }
    }

    public void limpaCampos() {
        lbCodigo.setText("...");
        tfNome.setText("");
        tfTelefone.setText("");
        tfEmail.setText("");
        tfCNPJ.setText("");
        tfanotacao.setText("");
        cbTipo.setSelectedIndex(0);

        tfRua.setText("");
        tfNumero.setText("");
        tfBairro.setText("");
        tfCidade.setText("");
        tfEstado.setText("");
        tfPais.setText("");

        btExcluir.setEnabled(false);
        btEditar.setEnabled(false);

    }
    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        if (verificaCampos() == false) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        } else {
            FornecedorBEAN f = new FornecedorBEAN();
            f.setForNomeEmpresa(tfNome.getText());
            f.setForTelefoneContato(tfTelefone.getText());
            f.setForEmail(tfEmail.getText());
            f.setForCNPJ(tfCNPJ.getText());
            f.setForAnotacoes(tfanotacao.getText());

            EnderecoBEAN end = new EnderecoBEAN();
            end.setEndRua(tfRua.getText());
            end.setEndNumero(Integer.parseInt(tfNumero.getText()));
            end.setEndBairro(tfBairro.getText());
            end.setEndCidade(tfCidade.getText());
            end.setEndEstado(tfEstado.getText());
            end.setEndPais(tfPais.getText());

            f.setEndereco(end);

            forCon.cadastrar(f);
            this.preencheTabela();
            limpaCampos();
        }
    }//GEN-LAST:event_btCadastrarActionPerformed

    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        if (verificaCampos() == false) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        } else {
            FornecedorBEAN f = forCon.localizar(Integer.parseInt(lbCodigo.getText()));
            f.setForNomeEmpresa(tfNome.getText());
            f.setForTelefoneContato(tfTelefone.getText());
            f.setForEmail(tfEmail.getText());
            f.setForCNPJ(tfCNPJ.getText());
            f.setForAnotacoes(tfanotacao.getText());

            f.getEndereco().setEndRua(tfRua.getText());
            f.getEndereco().setEndNumero(Integer.parseInt(tfNumero.getText()));
            f.getEndereco().setEndBairro(tfBairro.getText());
            f.getEndereco().setEndCidade(tfCidade.getText());
            f.getEndereco().setEndEstado(tfEstado.getText());
            f.getEndereco().setEndPais(tfPais.getText());

            forCon.editar(f);

            this.preencheTabela();
            limpaCampos();
        }
    }//GEN-LAST:event_btEditarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (lbCodigo.getText().equals("...")) {
            JOptionPane.showMessageDialog(null, "É preciso ter localizado um Forcenedor!");
        } else {
            forCon.remover(Integer.parseInt(lbCodigo.getText()));
            limpaCampos();
            this.preencheTabela();
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void tableFornecedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableFornecedorMouseClicked
        limpaCampos();
        if (tableFornecedor.getSelectedRow() != -1) {
            FornecedorBEAN f = forCon.localizar(Integer.parseInt(tableFornecedor.getValueAt(tableFornecedor.getSelectedRow(), 0).toString()));

            lbCodigo.setText(String.valueOf(f.getForCodigo()));
            tfNome.setText(f.getForNomeEmpresa());
            tfTelefone.setText(f.getForTelefoneContato());
            tfEmail.setText(f.getForEmail());
            tfCNPJ.setText(f.getForCNPJ());
            tfanotacao.setText(f.getForAnotacoes());               
            
            tfRua.setText(f.getEndereco().getEndRua());
            tfNumero.setText(String.valueOf(f.getEndereco().getEndNumero()));
            tfBairro.setText(f.getEndereco().getEndBairro());
            tfCidade.setText(f.getEndereco().getEndCidade());
            tfEstado.setText(f.getEndereco().getEndEstado());
            tfPais.setText(f.getEndereco().getEndPais());

            btExcluir.setEnabled(true);
            btEditar.setEnabled(true);
        }
    }//GEN-LAST:event_tableFornecedorMouseClicked

