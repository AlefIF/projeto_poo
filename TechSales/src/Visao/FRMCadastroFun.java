    private void cadastrar2() {
        FuncionarioBEAN c = new FuncionarioBEAN();
        c.setFunNome(tfNome.getText());
        c.setFunCpf(tfCpf.getText());
        c.setFunIdade(Integer.parseInt(tfIdade.getText()));
        c.setFunTelefone(tfTelefone.getText());
        c.setFunNisPis(tfNPIS.getText());
        EmpregoBEAN e = (EmpregoBEAN) cbEmprego.getSelectedItem();
        c.setEmprego(e);
        c.setFunSalario(Float.parseFloat(tfSalario.getText()));
        c.setFunEmail(tfEmail.getText());

        EnderecoBEAN end = new EnderecoBEAN();
        end.setEndRua(tfRua.getText());
        end.setEndNumero((tfNumero.getText()));
        end.setEndBairro(tfBairro.getText());
        end.setEndCidade(tfCidade.getText());
        end.setEndEstado(tfEstado.getText());
        end.setEndPais(tfPais.getText());

        c.setEndereco(end);

        CaixaBEAN ca = cCaixa.localizar(1);
        c.setCaixa(ca);

        ct.cadastrar(c);
        JOptionPane.showMessageDialog(null, "Cadastrado com sucesso");

        cadVendedor(c, e);
    }

    private void cadastrar() {
        int c = verificaCPF();
        int e = verificapis();

        if (verificaCampos() == false) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        } else if (c == 0) {
            if (e == 0) {
                cadastrar2();
                this.preencheTabela();
                limpaCampos();
            } else {
                JOptionPane.showMessageDialog(null, "Pis já cadastrado no sistema.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Cpf  já cadastrado no sistema.");
        }

    }

    private void cadVendedor(FuncionarioBEAN c, EmpregoBEAN e) {
        if (e.getEmpNome().equals("Vendedor") || e.getEmpNome().equals("Administrador")) {
            if (tfNomeUser.getText().equals("") || tfSenhaUser.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Login não cadastrado, selecione o funcionario, clique em editar e tente gravar o login novamente");
            } else {
                VendedorBEAN v = new VendedorBEAN();
                v.setFuncionario(c);
                v.setVendedorNomeUsuario(tfNomeUser.getText());
                v.setVendedorSenha(tfSenhaUser.getText());
                vc.cadastrar(v);
            }
        }
        limpaCampos();
        limpaBotoes();
    }

    private void editar2() {
        FuncionarioBEAN c = ct.localizarCodigo(Integer.parseInt(lbCodigo.getText()));
        c.setFunNome(tfNome.getText());
        c.setFunCpf(tfCpf.getText());
        c.setFunIdade(Integer.parseInt(tfIdade.getText()));
        c.setFunTelefone(tfTelefone.getText());
        c.setFunNisPis(tfNPIS.getText());
        EmpregoBEAN e = (EmpregoBEAN) cbEmprego.getSelectedItem();
        c.setEmprego(e);
        c.setFunSalario(Float.parseFloat(tfSalario.getText()));
        c.setFunEmail(tfEmail.getText());

        c.getEndereco().setEndRua(tfRua.getText());
        c.getEndereco().setEndNumero(tfNumero.getText());
        c.getEndereco().setEndBairro(tfBairro.getText());
        c.getEndereco().setEndCidade(tfCidade.getText());
        c.getEndereco().setEndEstado(tfEstado.getText());
        c.getEndereco().setEndPais(tfPais.getText());

        ct.editar(c);

        JOptionPane.showMessageDialog(null, "Funcionário Editado com sucesso");

        if (e.getEmpNome().equals("Vendedor")) {
            for (VendedorBEAN a : vc.listarALL()) {
                if (a.getFuncionario().getFunCodigo() == c.getFunCodigo()) {
                    VendedorBEAN v = vc.localizar(a.getVendedorCodigo());
                    v.setVendedorNomeUsuario(tfNomeUser.getText());
                    v.setVendedorSenha(tfSenhaUser.getText());
                    vc.editar(v);
                }
            }
        }
        limpaCampos();
        limpaBotoes();
    }

    public boolean verificaCampos() {
        if (tfNome.getText().equals("") || tfIdade.getText().equals("")
                || tfCpf.getText().equals("")
                || tfNPIS.getText().equals("") || tfTelefone.getText().equals("")
                || cbEmprego.getSelectedIndex() == 0
                || tfEmail.getText().equals("")
                || tfRua.getText().equals("")
                || tfNumero.getText().equals("")
                || tfBairro.getText().equals("")
                || tfCidade.getText().equals("")
                || tfEstado.getText().equals("")
                || tfPais.getText().equals("")) {
            return false;
        } else {
            return true;
        }
    }

    public void limpaCampos() {
        lbCodigo.setText("...");
        tfNome.setText("");
        tfCpf.setText("");
        tfIdade.setText("");
        tfTelefone.setText("");
        tfNPIS.setText("");
        cbEmprego.setSelectedIndex(0);
        tfSalario.setText("");
        tfEmail.setText("");

        tfNomeUser.setText("");
        tfSenhaUser.setText("");

        tfRua.setText("");
        tfNumero.setText("");
        tfBairro.setText("");
        tfCidade.setText("");
        tfEstado.setText("");
        tfPais.setText("");

        btEditar.setEnabled(false);

        tfNomeUser.setEnabled(false);
        tfSenhaUser.setEnabled(false);
    }

    private DefaultTableModel criaTabela() {
        //sempre que usar JTable é necessário ter um DefaulttableModel
        DefaultTableModel tbUsers = new DefaultTableModel() {
            //Define o tipo dos campos (coluna) na mesma ordem que as colunas foram criadas
            Class[] types = new Class[]{
                java.lang.Integer.class, java.lang.String.class, java.lang.String.class,
                java.lang.String.class, java.lang.String.class, java.lang.String.class,
                java.lang.String.class, java.lang.String.class,};
            //define se os campos podem ser editados na propria tabela
            boolean[] canEdit = new boolean[]{
                false, false, false, false, false, false, false, false
            };

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        ;

        };
        //retorna o DefaultTableModel
    return tbUsers;
    }

