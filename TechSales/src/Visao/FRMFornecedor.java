    private DefaultTableModel criaTabela() {
        DefaultTableModel dTable = new DefaultTableModel() {
            Class[] types = new Class[]{
                java.lang.Integer.class,
                java.lang.String.class,
                java.lang.String.class,
                java.lang.Double.class,
                java.lang.String.class,
                java.lang.String.class,
                java.lang.String.class,
                java.lang.String.class,};
            boolean[] canEdit = new boolean[]{
                false, false, false, false, false, false, false, false};

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        ;

        };
    return dTable;
    }

    private void preencheTabela() {
        dTable = criaTabela();
        dTable.addColumn("Código");
        dTable.addColumn("Nome");
        dTable.addColumn("Telefone");
        dTable.addColumn("Email");
        dTable.addColumn("CNPJ/CPF");
        dTable.addColumn("Anotações");
        fDados = forCon.listarALL();

        for (FornecedorBEAN d : fDados) {
            dTable.addRow(new Object[]{d.getForCodigo(), d.getForNomeEmpresa(),
                d.getForTelefoneContato(), d.getForEmail(), d.getForCNPJ(),
                d.getForAnotacoes()});
        }

        tableFornecedor.setModel(dTable);
    }

    public boolean verificaCampos() {
        if (tfNome.getText().equals("")
                || tfEmail.getText().equals("") || tfTelefone.getText().equals("")
                || tfCNPJ.getText().equals("   .   .   -  ") || tfCNPJ.getText().equals("   .   .   /    -  ")
                || tfCNPJ.getText().equals("")
                || tfanotacao.getText().equals("")
                || tfRua.getText().equals("")
                || tfNumero.getText().equals("")
                || tfBairro.getText().equals("")
                || tfCidade.getText().equals("")
                || tfEstado.getText().equals("")
                || tfPais.getText().equals("")) {

            return false;
        } else {
            return true;
        }
    }

    public void limpaCampos() {
        lbCodigo.setText("...");
        tfNome.setText("");
        tfTelefone.setText("");
        tfEmail.setText("");
        tfCNPJ.setText("");
        tfanotacao.setText("");
        cbTipo.setSelectedIndex(0);

        tfRua.setText("");
        tfNumero.setText("");
        tfBairro.setText("");
        tfCidade.setText("");
        tfEstado.setText("");
        tfPais.setText("");

    }

    private int verificaCPFCNPJ() {
        int cod = 0;
        for (FornecedorBEAN f : forCon.listarALL()) {
            if (f.getForCNPJ().equals(tfCNPJ.getText())) {
                try {
                    if (f.getForCodigo() != (Integer.valueOf(lbCodigo.getText()))) {
                        cod = f.getForCodigo();
                    }
                } catch (Exception e) {
                    cod = 1;
                }

            }
        }
        return cod;
    }

    private void cad() {
        int c = verificaCPFCNPJ();
        if (c == 0) {
            FornecedorBEAN f = new FornecedorBEAN();
            f.setForNomeEmpresa(tfNome.getText());
            f.setForTelefoneContato(tfTelefone.getText());
            f.setForEmail(tfEmail.getText());
            f.setForCNPJ(tfCNPJ.getText());
            f.setForAnotacoes(tfanotacao.getText());

            EnderecoBEAN end = new EnderecoBEAN();
            end.setEndRua(tfRua.getText());
            end.setEndNumero(tfNumero.getText());
            end.setEndBairro(tfBairro.getText());
            end.setEndCidade(tfCidade.getText());
            end.setEndEstado(tfEstado.getText());
            end.setEndPais(tfPais.getText());

            f.setEndereco(end);

            forCon.cadastrar(f);
            limpaCampos();
            limpaBotoes();
            desabilitaCampos();
            preencheTabela();
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso");

        } else {
            JOptionPane.showMessageDialog(null, "Cpf ou CNPJ já cadastrado no sistema.");
        }

    }
    private void btGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGravarActionPerformed
        if (verificaCampos()) {
            if (lbCodigo.getText().equals("...")) {
                edit();
            } else {
                cad();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        }

    }//GEN-LAST:event_btGravarActionPerformed

    private void edit() {
        int c = verificaCPFCNPJ();
        if (c == (Integer.valueOf(lbCodigo.getText()))) {
            c = 0;
        }
        if (c == 0) {
            FornecedorBEAN f = forCon.localizar(Integer.parseInt(lbCodigo.getText()));
            f.setForNomeEmpresa(tfNome.getText());
            f.setForTelefoneContato(tfTelefone.getText());
            f.setForEmail(tfEmail.getText());
            f.setForCNPJ(tfCNPJ.getText());
            f.setForAnotacoes(tfanotacao.getText());

            f.getEndereco().setEndRua(tfRua.getText());
            f.getEndereco().setEndNumero(tfNumero.getText());
            f.getEndereco().setEndBairro(tfBairro.getText());
            f.getEndereco().setEndCidade(tfCidade.getText());
            f.getEndereco().setEndEstado(tfEstado.getText());
            f.getEndereco().setEndPais(tfPais.getText());

            forCon.editar(f);
            limpaCampos();
            limpaBotoes();
            desabilitaCampos();
            preencheTabela();
            JOptionPane.showMessageDialog(null, "Editado com sucesso");
        } else {
            JOptionPane.showMessageDialog(null, "Cpf ou CNPJ já cadastrado no sistema. Código: " + c);
        }
    }

