    private int retornaLinha() {
        //retorna a linha que o usuário selecionou na tabela
        int linha = tableConsole.getSelectedRow();
        return linha;
    }

    private void pegaSelecionado() {
        int linha = retornaLinha();
        cDados = cCon.listarALL();
        lbCodCon.setText(String.valueOf(cDados.get(linha).getConCodigo()));
        tfNome.setText(cDados.get(linha).getConNome());
        tfMarca.setText(cDados.get(linha).getConMarca());
    }
