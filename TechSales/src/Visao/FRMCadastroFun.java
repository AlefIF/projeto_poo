    private int verificaCPF() {
        int i = 0;
        for (FuncionarioBEAN c : ct.listarALL()) {
            if (c.getFunCpf().equals(tfCpf.getText())) {
                try {
                    if (c.getFunCodigo() != (Integer.valueOf(lbCodigo.getText()))) {
                        i = c.getFunCodigo();
                    }
                } catch (Exception e) {
                    i = 1;
                }

            }
        }
        return i;
    }

