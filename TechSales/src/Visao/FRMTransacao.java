    private void preencheTabelaLoc(int c) {
        modelLoc = criaTabelaLoc();
        modelLoc.addColumn("Código-Locação");
        modelLoc.addColumn("Código-Jogo");
        modelLoc.addColumn("Jogo");
        modelLoc.addColumn("Quantidade");
        modelLoc.addColumn("Entrada");
        modelLoc.addColumn("Data-Aluguel");
        modelLoc.addColumn("Data-Devolução");

        for (ClienteBEAN cli : contCli.listarALL()) {
            for (LocacaoBEAN loc : contLoc.listarALL()) {
                if (cli.getCliCodigo() == loc.getCliente().getCliCodigo()) {
                    for (JogoLocacaoBEAN jl : cJl.listarALL()) {
                        if (loc.getLocCodigo() == jl.getChaveComposta().getLocacao().getLocCodigo()) {
                            for (JogoBEAN jogo : contJog.listarALL()) {
                                if (jogo.getJoCodigo() == jl.getChaveComposta().getJogo().getJoCodigo()) {
                                    if (loc.getLocStatus() == 0) {
                                        modelLoc.addRow(new Object[]{loc.getLocCodigo(), jogo.getJoCodigo(), jogo.getJoNome(), jl.getJlQtd(),loc.getLocEntrada(), loc.getLocDataAluguel(), loc.getLocDataDevolucao()});
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
        tbLoc.setModel(modelLoc);
    }

